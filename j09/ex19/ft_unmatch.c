int	ft_unmatch(int *tab, int length)
{
	int	i;
	int	c;

	i = 0;
	while (i < length)
	{
		while (c < length)
		{
			if (tab[i] == tab[c])
				c++;
			if (c % 2 != 0)
				return (tab[i])
			c++;
		}
		i++;
	}
	return (0);
}
